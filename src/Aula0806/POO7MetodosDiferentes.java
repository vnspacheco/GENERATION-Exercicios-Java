package Aula0806;

public class POO7MetodosDiferentes {
	
	//Método do tipo inteiro (recebe número inteiros)
	
	public int soma(int numero1, int numero2) {
		return numero1 + numero2;
	}
	
	//Método do tipo double (recebe números double)
	
	public double soma(double numero1, double numero2) {
		return numero1 + numero2;
	}
	
	//Método do tipo int com parametros diferentes
	
	public int soma(int numero1, int numero2, int numero3) {
		return numero1 + numero2 + numero3;
	}
}
